package store

import (
	"testing"
)

/////////////////////////////////////////////////////////////////////////////////////////////////////
// Create a Basic Store for testing
/////////////////////////////////////////////////////////////////////////////////////////////////////
func createStore(t *testing.T) *Store {
	st, err := NewStore(10, "tcp", ":6379", "")

	if err != nil {
		t.Error("testing store: cannot create store :: ", err.Error())
		t.Fail()
	}

	return st
}
